const SIMPLE = `better-ai`

export function renderHeader(): string {
  const terminalWidth = process.stdout.columns ?? 80
  const logoWidth = Math.max(...LOGO.split("\n").map((l) => l.length))

  const title = terminalWidth >= logoWidth ? pc.cyan(LOGO) : pc.bold(pc.cyan(SIMPLE))
  const subtitle = pc.dim(`  v${pkg.version} ‚ÄĒ ${pkg.description}`)

  return `${title}\n${subtitle}\n`
}
